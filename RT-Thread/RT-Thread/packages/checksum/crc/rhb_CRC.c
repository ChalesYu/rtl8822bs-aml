/*********************************************************************************************************
** Function name:       rhb_CRC16
** Descriptions:        计算指定数据量的CRC校验结果
** input parameters:    pu8Msg：指定数据起始地址
**                      u32Len： 指定数据长度
**                      CRC16:  CRC初值，用于分段校验CRC。当一次检验所有的数据时，设置为0
** output parameters:   NONE
** Returned value:      CRC16校验结果
*********************************************************************************************************/
rt_uint16_t rhb_CRC16(rt_uint8_t *pu8Msg, rt_uint32_t u32Len, rt_uint16_t CRC16)
{
    rt_uint32_t i;
    
    for(i=0; i<u32Len; i++)
    {
      CRC16 = CRC16_Table[((CRC16 & 0xFF)^(*pu8Msg++))]^(CRC16 >> 8);
    }
    return(CRC16);
}

/*********************************************************************************************************
** Function name:       rhb_ITU_CRC16
** Descriptions:        计算指定数据量的CRC校验结果,ITU标准的多项式
** input parameters:    pu8Msg：指定数据起始地址
**                      u32Len： 指定数据长度
**                      CRC16:  CRC初值，用于分段校验CRC。当一次检验所有的数据时，设置为0
** output parameters:   NONE
** Returned value:      CRC16校验结果
*********************************************************************************************************/
rt_uint16_t rhb_ITU_CRC16(rt_uint8_t *pu8Msg, rt_uint32_t u32Len, rt_uint16_t CRC16)
{
    rt_uint32_t i;

    for (i = 0;  i < u32Len;  i++) {
        CRC16 = CRC16_ITU_Table[((CRC16>>8) ^ *pu8Msg++) & 0xFF] ^ (CRC16 << 8);
    }
    return CRC16;
}

/*********************************************************************************************************
** Function name:       rhb_CRC32
** Descriptions:        计算指定数据量的CRC校验结果
** input parameters:    pu8Msg：指定数据起始地址
**                      u32Len 指定数据长度
**                      CRC32:  CRC初值，用于分段校验CRC。当一次检验所有的数据时，设置为0
**                      
** output parameters:   NONE
** Returned value:      CRC32校验结果
*********************************************************************************************************/
rt_uint32_t rhb_CRC32(rt_uint8_t *pu8Msg, rt_uint32_t u32Len, rt_uint32_t CRC32)
{
  rt_uint32_t i;

  for (i = 0;  i < u32Len;  i++) {
    CRC32 = CRC32_Table[(CRC32 ^ pu8Msg[i]) & 0xff] ^ (CRC32 >> 8);
  }
  return CRC32;
}

/*********************************************************************************************************
** Function name:       rhb_ethernet_CRC32
** Descriptions:        计算指定数据量的CRC校验结果
** input parameters:    pu8Msg：指定数据起始地址
**                      u32Len 指定数据长度
**                      CRC32:  CRC初值，用于分段校验CRC。当一次检验所有的数据时，设置为0
**                      
** output parameters:   NONE
** Returned value:      CRC32校验结果
*********************************************************************************************************/
rt_uint32_t rhb_ethernet_CRC32(rt_uint8_t *pu8Msg, rt_uint32_t u32Len, rt_uint32_t CRC32)
{
  rt_uint32_t i;

  CRC32 = CRC32 ^ 0xffffffff;
  for (i = 0;  i < u32Len;  i++) {
    CRC32 = CRC32_Table[(CRC32 ^ pu8Msg[i]) & 0xff] ^ (CRC32 >> 8);
  }
  return (CRC32 ^ 0xffffffff);
}

